using namespace std;

using Eigen::MatrixXd;
using Eigen::VectorXd;

KalmanFilter::KalmanFilter() {}

KalmanFilter::~KalmanFilter() {}

void KalmanFilter::Init(MatrixXd &H_in, MatrixXd &R_in) {
  H_ = H_in;
  R_ = R_in;
}

